field_map = {
    "calories_per_100g": "calories",
    "protein_per_100g": "protein_g",
    "fat_per_100g": "fat_g",
    "carbohydrates_per_100g": "carbs_g",
    "sugar_per_100g": "sugar_g",
    "calories_per_100ml": "calories",
    "protein_per_100ml": "protein_g",
    "fat_per_100ml": "fat_g",
    "carbohydrates_per_100ml": "carbs_g",
    "sugar_per_100ml": "sugar_g",
}
field_map_lower = {k.lower(): v for k, v in field_map.items()}


def is_nutrient_dict(obj: object) -> bool:
    if not isinstance(obj, dict):
        return False
    keys = {k.lower() for k in obj.keys()}
    return any(k in field_map_lower for k in keys)


def infer_basis_from_dict(data: dict) -> str:
    keys_lower = {k.lower() for k in data.keys()}
    if any("per_100ml" in k for k in keys_lower):
        return "per_100ml"
    return "per_100g"


def iter_flat_items(prefix: str, data: dict):
    """
    Ï§ëÏ≤© ÎîïÏÖîÎÑàÎ¶¨Î•º ÌèâÌÉÑÌôîÌïòÏó¨ (Ìï≠Î™©Î™Ö, ÏòÅÏñë dict)ÏùÑ yield.
    """
    for name, value in data.items():
        if not isinstance(value, dict):
            continue
        combined_name = name if not prefix else f"{prefix} - {name}"
        if is_nutrient_dict(value):
            yield combined_name, value
        else:
            yield from iter_flat_items(combined_name, value)


def normalize_one_category(category: str, data: dict) -> pd.DataFrame:
    if not data:
        return pd.DataFrame(
            columns=["category", "item", "prep_state", "basis", "calories", "protein_g", "fat_g", "carbs_g", "sugar_g"]
        )

    rows = []
    for item_name, nutrient_dict in iter_flat_items("", data):
        if not nutrient_dict:
            continue

        basis = infer_basis_from_dict(nutrient_dict)
        prep = nutrient_dict.get("prep") or infer_prep_state(item_name)

        row = {
            "category": category,
            "item": item_name,
            "prep_state": prep,
            "basis": basis,
            "calories": pd.NA,
            "protein_g": pd.NA,
            "fat_g": pd.NA,
            "carbs_g": pd.NA,
            "sugar_g": pd.NA,
        }

        for key, value in nutrient_dict.items():
            mapped = field_map_lower.get(key.lower())
            if mapped is not None:
                row[mapped] = value

        rows.append(row)

    if not rows:
        return pd.DataFrame(
            columns=["category", "item", "prep_state", "basis", "calories", "protein_g", "fat_g", "carbs_g", "sugar_g"]
        )

    df = pd.DataFrame(rows)
    df = df[["category", "item", "prep_state", "basis", "calories", "protein_g", "fat_g", "carbs_g", "sugar_g"]]
    return df

